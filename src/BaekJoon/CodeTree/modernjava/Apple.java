package BaekJoon.CodeTree.modernjava;

public class Apple {

    private String color;

    private int weight;

    private boolean handsome;

    public Apple(String color, int weight, boolean handsome){
        this.color = color;
        this.weight = weight;
        this.handsome = handsome;
    }

    public String getColor() {
        return color;
    }

    public int getWeight() {
        return weight;
    }

    public boolean isHandsome() {
        return handsome;
    }

}
